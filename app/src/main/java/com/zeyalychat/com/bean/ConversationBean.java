package com.zeyalychat.com.bean;

public class ConversationBean {

    String content;
    String created_time;
    String has_attachments;
    String id;
    String is_sender;
    String message_id;
    String thread_id;
    String name;
    String userid;
    String profile_image_url;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCreated_time() {
        return created_time;
    }

    public void setCreated_time(String created_time) {
        this.created_time = created_time;
    }

    public String getHas_attachments() {
        return has_attachments;
    }

    public void setHas_attachments(String has_attachments) {
        this.has_attachments = has_attachments;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIs_sender() {
        return is_sender;
    }

    public void setIs_sender(String is_sender) {
        this.is_sender = is_sender;
    }

    public String getMessage_id() {
        return message_id;
    }

    public void setMessage_id(String message_id) {
        this.message_id = message_id;
    }

    public String getThread_id() {
        return thread_id;
    }

    public void setThread_id(String thread_id) {
        this.thread_id = thread_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getProfile_image_url() {
        return profile_image_url;
    }

    public void setProfile_image_url(String profile_image_url) {
        this.profile_image_url = profile_image_url;
    }



}
