package com.zeyalychat.com.bean;

public class AttendanceBean {
    String afternoon_session;
    String attendance_status;
    String id;
    String morning_session;
    String name;
    public String getAfternoon_session() {
        return afternoon_session;
    }

    public void setAfternoon_session(String afternoon_session) {
        this.afternoon_session = afternoon_session;
    }

    public String getAttendance_status() {
        return attendance_status;
    }

    public void setAttendance_status(String attendance_status) {
        this.attendance_status = attendance_status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMorning_session() {
        return morning_session;
    }

    public void setMorning_session(String morning_session) {
        this.morning_session = morning_session;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
